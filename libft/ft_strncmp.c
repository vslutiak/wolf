#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t num)
{
	size_t i;

	i = 0;
	if (num == 0)
		return (0);
	while (i < (num - 1) && s1[i] == s2[i] && s1[i] && s2[i])
		i++;
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
