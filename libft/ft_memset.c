#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*d;

	i = 0;
	d = (char *)b;
	while (len > i)
	{
		d[i] = c;
		i++;
	}
	return (d);
}
